void main() {


  // Arithmetic Operators
print("Arithmetic Operators");
  int a = 10;
  int b = 3;

  print(a + b);    // 13
  print(a - b);    // 7
  print(a * b);    // 30
  print(a / b);    // 3.333...
  print(a % b);    // 1
  print(a ~/ b);   // 3 (integer division)


  // Comparison Operators
print("Comparison Operator");
  int x = 5;
  int y = 10;

  print(x == y);  // false
  print(x != y);  // true
  print(x < y);   // true

  // Logical Operators

  print("Logical Operators");

  bool M = true;
  bool N = false;

  print(M && N);  // false
  print(M || N);  // true
  print(!M);      // false
}
